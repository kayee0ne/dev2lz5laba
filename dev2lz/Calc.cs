namespace Dev;

public class Calc
{
    public int Sum(int a, int b)
    {
        return (a + b);
    }

    public int Subtract(int a, int b)
    {
        return (a - b);
    }

    public int Mult(int a, int b)
    {
        return (a * b);
    }

    public int Div(int a, int b)
    {
        return (a / b);
    }
}